        <h1><i>Ocarina of Time</i></h1>
        <p><i>Ocarina of Time</i> was the first Legend of Zelda game made for the Nintendo 64. It was a monumental task to accomplish for Nintendo's staff, as, at the time, most of the development team was inexperienced in developing 3D video games. However, after 4 years of development, it became one of Nintendo's biggest successes ever.</p>
        <h2>Plot</h2>
        <p>Link, the protagonist, is visited by a fairy named Navi and told to go to the Great Deku Tree, who has been cursed. Before he dies, the tree gives Link the Kokiri Emerald, one of three Spiritual Stones that must be collected to gain access to the Temple of Time, where the Triforce and the Master Sword are located. He then visits the princess of the kingdom, Zelda, who is suspicious of Ganondorf, leader of a neighboring kingdom, whom she believes wants the Triforce, an omnipotent relic. Zelda urges Link to acquire the Spiritual Stones. Link does this, and enters the Temple of Time. Ganondorf, however, sneaks in behind him.</p>
        <p>Meanwhile, Link gets the Master Sword and is transported forward seven years. A spirit then explains that Ganondorf has taken over Hyrule due to stealing the Triforce, and has sealed away five of the seven sages who can seal him away forever. Link manages to reawaken these sages' spirits with the help of the mysterious warrior Sheik. With the sages reassembled, Link approaches Ganondorf's castle, where Sheik reveals herself to be Zelda. After the final battle with Ganon, a Triforce-enhanced form of Ganondorf, Link is sent back in time to his childhood, where he helps Zelda prevent Ganon's uprising before it happens.</p>
        <h2>Notable Elements</h2>
        <h3>Ocarina</h3>
        <p>Near the beginning of the game, Link acquires a Fairy Ocarina, which is only used before collecting all three Spiritual Stones and is primarily used for contacting Saria, one of Link's friends. However, it is soon replaced by the Ocarina of Time, which can change day into night, summon rainfall, warp Link to several places, and most importantly unlock the Door of Time.</p>
